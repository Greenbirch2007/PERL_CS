    }
    foreach my $dept ( keys %depts ) {
        $header .=
            "; number of hosts in the $dept department: " . "$depts{$dept}.\n";
    }
    $header .=
        '; total number of hosts: ' . scalar( keys %entries ) . "\n;\n\n";
